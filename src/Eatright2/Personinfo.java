package Eatright2;

public class Personinfo{
    String weightgain;
    String gender;
    int age;
    int weight;
    int height;
    int userGain;

        public void setWeightgain(String weightgain) {
            this.weightgain = weightgain;
        }

        public void setGender(String gender) {
            this.gender = gender;
        }

        public void setAge(int age) {
            this.age = age;
        }

        public void setWeight(int weight) {
            this.weight = weight;
        }

        public void setHeight(int height) {
            this.height = height;
        }

        public void setUserGain(int userGain) {
            this.userGain = userGain;
        }


        public String getWeightgain() {
            return this.weightgain;
        }

        public String getGender() {
            return this.gender;
        }

        public Integer getAge() {
            return this.age;
        }

        public Integer getWeight() {
            return this.weight;
        }

        public Integer getHeight() {
            return this.height;
        }

        public Integer getUserGain() {
            return this.userGain;
        }

}

